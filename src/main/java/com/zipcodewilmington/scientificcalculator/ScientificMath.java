package com.zipcodewilmington.scientificcalculator;

public class ScientificMath {

    public static double sine(int x){
        double output = Math.sin((double)x);
        return output;
    }

    public static double cosine(int x){
        double output = Math.cos((double)x);
        return output;
    }

    public static double tan(int x){
        double output = Math.tan((double)x);
        return output;
    }
    public static double inverseSine(int x){
        double output = Math.asin((double)x);
        return output;
    }

    public static double inverseCosine(int x){
        double output = Math.acos((double)x);
        return output;
    }

    public static double inverseTan(int x){
        double output = Math.atan((double)x);
        return output;
    }


}
